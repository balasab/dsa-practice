const wordBreak = (word, wordDict) => {
    const wordSet = new Set(wordDict);
    const n = word.length;
    const dp = new Array(n + 1).fill(false);
    dp[0] = true;

    for (let i = 1; i <= n; i++) {
        for (let j = 0; j < i; j++) {
            if (dp[j] && wordSet.has(word.slice(j, i))){
                dp[i] = true;
                break;
            }
        }
    }
    return dp[n];
}